<h4> Information about TNMS </h4></br>
This version only provides basic switch and port management and limited integration with DMS system<br>
But TNMS can do more, it is a full functional network management system which can support:<br>
<div>
<ul>
  <li>support auto-discovery</li>
  <li>support topology discovery</li>
  <li>support bgp, ospf device poll</li>
  <li>support alarm customization / template</li>
  <li>support dashbard customization</li>
  <li>support service/app management and monitor</li>
  <li>support plugin development</li>
</ul>
</div>
